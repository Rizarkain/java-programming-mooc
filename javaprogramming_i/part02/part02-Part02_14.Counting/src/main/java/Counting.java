
import java.util.Scanner;

public class Counting {
    public static void printStars(int number) { 
        int i = 0;
        while (i <= number) {
            System.out.println("*");
            i++;
        }
        
    }
    public static void main(String[] args) {
        printStars(5);
        printStars(3);
        printStars(9);
    }   
                                                   

}

